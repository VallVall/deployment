export const sayHi = (number) => {
  switch (number) {
    case 1:
      return 'Hi!';
    case 2:
      return 'Hello';
    case 3:
      return 'Good evening!';
    case 4:
      return 'Have a good morning';
    case 5:
      return 'Hey!';
    case 6:
      return 'Bonjur';
    case 7:
      return 'Bless you!';

    default:
      return 'No answer!';
  }
};
